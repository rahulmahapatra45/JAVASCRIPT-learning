console.log(Math);  // Object [Math] {}
console.log(Math.abs(-800)); // print positive value ex:- -800 -> 800
console.log(Math.round(4.5)); // 4.5 -> 5 and 4.4 -> 4
console.log(Math.sqrt(25)); // square root of 25 is 5
console.log(Math.pow(10,3)); // power of 10,2  is 100
console.log(Math.min(2,7,9,1,0)) // print lowest
console.log(Math.max(2,7,9,1,0)) // print highest


console.log(Math.random()) // always gives value between 0 and 1 
console.log((Math.random() * 10) + 1); // gives value between 1 and 10
console.log(Math.floor(Math.random()*10 ) +1) // gives value between 1 and 10 here but take it a minimum round figure

const min = 10 
const max = 20
console.log(Math.floor(Math.random() * (max - min +1)) + min); // always gives value between min and max





